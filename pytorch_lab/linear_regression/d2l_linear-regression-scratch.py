# 回想一下，[**实现softmax**]由三个步骤组成：
# 1. 对每个项求幂（使用`exp`）；
# 1. 对每一行求和（小批量中每个样本是一行），得到每个样本的规范化常数；
# 1. 将每一行除以其规范化常数，确保结果的和为1。
def softmax(X):
    X_exp = torch.exp(X)
    partition = X_exp.sum(1, keepdim=True)
    return X_exp / partition  # 这里应用了广播机制


